import { defHttp } from '/@/utils/http/axios';
import { ContentTypeEnum, ResponseEnum } from '@/enums/httpEnum';
import { PageResult } from '@/api/model/pageResult';
import { QuerySettlePreSheetVo } from '@/api/settle/pre/model/querySettlePreSheetVo';
import { QuerySettlePreSheetBo } from '@/api/settle/pre/model/querySettlePreSheetBo';
import { GetSettlePreSheetBo } from '@/api/settle/pre/model/getSettlePreSheetBo';
import { CreateSettlePreSheetVo } from '@/api/settle/pre/model/createSettlePreSheetVo';
import { UpdateSettlePreSheetVo } from '@/api/settle/pre/model/updateSettlePreSheetVo';
import { ApprovePassSettlePreSheetVo } from '@/api/settle/pre/model/approvePassSettlePreSheetVo';
import { BatchApprovePassSettlePreSheetVo } from '@/api/settle/pre/model/batchApprovePassSettlePreSheetVo';
import { ApproveRefuseSettlePreSheetVo } from '@/api/settle/pre/model/approveRefuseSettlePreSheetVo';
import { BatchApproveRefuseSettlePreSheetVo } from '@/api/settle/pre/model/batchApproveRefuseSettlePreSheetVo';

const baseUrl = '/settle/presheet';
const region = 'cloud-api';

/**
 * 订单列表
 */
export function query(params: QuerySettlePreSheetVo): Promise<PageResult<QuerySettlePreSheetBo>> {
  return defHttp.get<PageResult<QuerySettlePreSheetBo>>(
    {
      url: baseUrl + '/query',
      params,
    },
    {
      region,
    },
  );
}

/**
 * 导出
 */
export function exportList(data: QuerySettlePreSheetVo): Promise<void> {
  return defHttp.post<void>(
    {
      url: baseUrl + '/export',
      data,
    },
    {
      region,
      responseType: ResponseEnum.BLOB,
      contentType: ContentTypeEnum.FORM_URLENCODED,
    },
  );
}

/**
 * 查询详情
 */
export function get(id: string): Promise<GetSettlePreSheetBo> {
  return defHttp.get<GetSettlePreSheetBo>(
    {
      url: baseUrl,
      params: {
        id,
      },
    },
    {
      region,
    },
  );
}

